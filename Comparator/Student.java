package Comparator;

public class Student {
    int i;
    String s;
    int i1;
    public Student(int i, String s, int i1) {
        this.i=i;
        this.s=s;
        this.i1=i1;

    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public int getI1() {
        return i1;
    }

    public void setI1(int i1) {
        this.i1 = i1;
    }
}
